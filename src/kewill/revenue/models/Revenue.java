package kewill.revenue.models;

public class Revenue {
private String blNo;
private double containerStuffingCharge;
private double vgmWeighingCharge;
private double landingCharge;
public String getBlNo() {
	return blNo;
}
public void setBlNo(String blNo) {
	this.blNo = blNo;
}
public double getContainerStuffingCharge() {
	return containerStuffingCharge;
}
public void setContainerStuffingCharge(double containerStuffingCharge) {
	this.containerStuffingCharge = containerStuffingCharge;
}
public double getVgmWeighingCharge() {
	return vgmWeighingCharge;
}
public void setVgmWeighingCharge(double vgmWeighingCharge) {
	this.vgmWeighingCharge = vgmWeighingCharge;
}
public double getLandingCharge() {
	return landingCharge;
}
public void setLandingCharge(double landingCharge) {
	this.landingCharge = landingCharge;
}

}

